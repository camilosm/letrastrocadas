	  <h4>Objetivo:</h4>
		O www.letrastrocadas.com.br pensou em você: que tem milhares de livros, que já leu e releu até perder o interesse, guardados em uma estante apenas acumulando poeira!
		<br>Já pensou em trocá-los por um que esteja na sua grande lista de “próximos livros que irei comprar”?
