cebe nos últimos descendentes das casas da Granja da Cruz dos Tordos e do Morro dos Ventos Uivantes o olhar de seus antepassados e a paixão entre os dois, morrendo só em sua loucura e solidão. Como último desejo é enterrado junto com Catarina, seu grande amor. Deste dia em diante muitos juram ver sempre um casal vagando pelas charnecas do Morro.
